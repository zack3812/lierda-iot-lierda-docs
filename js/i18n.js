const SUPPORTED_LANGS = ["zh", "en", "ko", "ja"];
const DEFAULT_LANG = "zh";

function detectLang() {
  const stored = localStorage.getItem("lierda-lang");
  if (stored && SUPPORTED_LANGS.includes(stored)) return stored;
  const browserLang = (navigator.language || "zh").substring(0, 2).toLowerCase();
  if (SUPPORTED_LANGS.includes(browserLang)) return browserLang;
  return DEFAULT_LANG;
}

function t(key) {
  return I18N[currentLang][key] || I18N[DEFAULT_LANG][key] || key;
}

